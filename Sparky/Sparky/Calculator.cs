namespace Sparky
{
    public class Calculator
    {   //Adding two numbers
        public int AddNumbers(int a, int b)
        {
            return a + b;
        }

        //Cheaking isOdd
        public bool IsOdd(int a)
        {
            return (a % 2 != 0);
        }

        //Adding two double Numbers

        public double AddTwoDoubles(double a, double b)
        {
            return a + b;
        }
    }


}